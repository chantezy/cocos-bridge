'use strict';

const fs = require('fs');
const path = require('path');

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const SCHEMA_TYPES = Object.freeze([
  'character_movement', 'combat_skill', 'level_enemy', 'economy_item',
  'element_matrix', 'character_defs', 'ui_screen', 'custom',
]);

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function pascalCase(str) {
  return str
    .replace(/(^|[_\s-])(\w)/g, function (_, __, ch) { return ch.toUpperCase(); })
    .replace(/[^a-zA-Z0-9]/g, '');
}

function camelCase(str) {
  const pascal = pascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function writeFileSafe(filePath, content) {
  ensureDir(path.dirname(filePath));
  fs.writeFileSync(filePath, content, 'utf8');
}

function fileExists(filePath) {
  try {
    return fs.statSync(filePath).isFile();
  } catch (_) {
    return false;
  }
}

function readFileSafe(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf8');
  } catch (_) {
    return '';
  }
}

// ---------------------------------------------------------------------------
// Type mapping
// ---------------------------------------------------------------------------

/**
 * Map a schema field type descriptor to a TypeScript type string.
 * Handles: float, int, string, bool, enum, array, and nested objects.
 */
function mapSchemaType(field) {
  if (!field || typeof field !== 'object') {
    return 'any';
  }

  var type = field.type;

  if (type === 'float' || type === 'int' || type === 'number') {
    return 'number';
  }
  if (type === 'string') {
    return 'string';
  }
  if (type === 'bool' || type === 'boolean') {
    return 'boolean';
  }
  if (type === 'enum') {
    if (Array.isArray(field.enumValues) && field.enumValues.length > 0) {
      return field.enumValues.map(function (v) { return "'" + String(v) + "'"; }).join(' | ');
    }
    return 'string';
  }
  if (type === 'array') {
    var itemType = field.itemType ? mapSchemaType({ type: field.itemType }) : 'any';
    return itemType + '[]';
  }
  if (type === 'object' && field.properties) {
    // Nested object — will be handled by the interface generator
    return 'object';
  }

  return 'any';
}

// ---------------------------------------------------------------------------
// TypeScript interface generation
// ---------------------------------------------------------------------------

/**
 * Generate a TypeScript interface string from schema data properties.
 * Recursively handles nested objects.
 */
function generateInterfaceProperties(properties, indent) {
  if (!properties || typeof properties !== 'object') {
    return '';
  }

  indent = indent || '  ';
  var lines = [];

  var keys = Object.keys(properties);
  for (var i = 0; i < keys.length; i++) {
    var key = keys[i];
    var field = properties[key];

    if (field && typeof field === 'object' && field.type === 'object' && field.properties) {
      // Nested interface
      var nestedName = pascalCase(key);
      lines.push(indent + '/** ' + (field.description || key) + ' */');
      lines.push(indent + key + ': {');
      lines.push(generateInterfaceProperties(field.properties, indent + '  '));
      lines.push(indent + '};');
    } else {
      var tsType = mapSchemaType(field);
      var optional = field && field.optional ? '?' : '';
      if (field && field.description) {
        lines.push(indent + '/** ' + field.description + ' */');
      }
      lines.push(indent + key + optional + ': ' + tsType + ';');
    }
  }

  return lines.join('\n');
}

function generateInterfaceFile(schemaType, schemaData) {
  var interfaceName = pascalCase(schemaType) + 'Config';
  var lines = [];

  lines.push('/**');
  lines.push(' * TypeScript interface for ' + schemaType + ' configuration.');
  lines.push(' * Generated by cocos-bridge config-generator.');
  lines.push(' * Target: Cocos Creator 3.8.x');
  lines.push(' */');
  lines.push('');

  // If schemaData has nested record types, generate sub-interfaces first
  if (schemaData && schemaData.recordType) {
    var recordInterfaceName = pascalCase(schemaType) + 'Record';
    lines.push('export interface ' + recordInterfaceName + ' {');
    if (schemaData.fields && typeof schemaData.fields === 'object') {
      lines.push(generateInterfaceProperties(schemaData.fields, '  '));
    } else if (schemaData.properties && typeof schemaData.properties === 'object') {
      lines.push(generateInterfaceProperties(schemaData.properties, '  '));
    }
    lines.push('}');
    lines.push('');
    lines.push('export interface ' + interfaceName + ' {');
    lines.push('  records: ' + recordInterfaceName + '[];');
    if (schemaData.version) {
      lines.push('  version?: number;');
    }
    lines.push('}');
  } else if (schemaData && typeof schemaData === 'object') {
    lines.push('export interface ' + interfaceName + ' {');
    lines.push(generateInterfaceProperties(schemaData, '  '));
    lines.push('}');
  } else {
    lines.push('export interface ' + interfaceName + ' {');
    lines.push('  [key: string]: any;');
    lines.push('}');
  }

  lines.push('');
  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// JSON config generation
// ---------------------------------------------------------------------------

function generateDefaultConfigData(schemaType, schemaData) {
  // If the user provided actual data, use it directly
  if (schemaData && schemaData.data) {
    return schemaData.data;
  }

  // Generate a sample structure from the schema definition
  if (schemaData && schemaData.fields) {
    var sample = {};
    var keys = Object.keys(schemaData.fields);
    for (var i = 0; i < keys.length; i++) {
      var key = keys[i];
      var field = schemaData.fields[key];
      sample[key] = generateDefaultValue(field);
    }

    if (schemaData.recordType) {
      return { records: [sample], version: schemaData.version || 1 };
    }
    return sample;
  }

  if (schemaData && schemaData.properties) {
    var props = {};
    var propKeys = Object.keys(schemaData.properties);
    for (var j = 0; j < propKeys.length; j++) {
      var pk = propKeys[j];
      props[pk] = generateDefaultValue(schemaData.properties[pk]);
    }
    return props;
  }

  // Fallback: return the raw schemaData if it looks like actual data
  return schemaData || {};
}

function generateDefaultValue(field) {
  if (!field || typeof field !== 'object') return null;

  var type = field.type;
  if (field.default !== undefined) return field.default;

  if (type === 'float' || type === 'int' || type === 'number') return 0;
  if (type === 'string') return '';
  if (type === 'bool' || type === 'boolean') return false;
  if (type === 'enum' && Array.isArray(field.enumValues) && field.enumValues.length > 0) {
    return field.enumValues[0];
  }
  if (type === 'array') return [];
  if (type === 'object' && field.properties) {
    var obj = {};
    var keys = Object.keys(field.properties);
    for (var i = 0; i < keys.length; i++) {
      obj[keys[i]] = generateDefaultValue(field.properties[keys[i]]);
    }
    return obj;
  }

  return null;
}

// ---------------------------------------------------------------------------
// ConfigLoader update
// ---------------------------------------------------------------------------

function generateConfigLoaderMethod(schemaType) {
  var interfaceName = pascalCase(schemaType) + 'Config';
  var configDir = schemaType;
  var methodName = camelCase('load_' + schemaType);
  var lines = [];

  lines.push('');
  lines.push('  /** Load ' + schemaType + ' configuration. */');
  lines.push('  public static async ' + methodName + '(): Promise<' + interfaceName + '> {');
  lines.push('    return ConfigLoader.load<' + interfaceName + '>("Configs/' + configDir + '");');
  lines.push('  }');

  return lines.join('\n');
}

function updateConfigLoader(loaderPath, schemaType) {
  var existing = readFileSafe(loaderPath);
  var method = generateConfigLoaderMethod(schemaType);
  var interfaceName = pascalCase(schemaType) + 'Config';

  // If the loader doesn't exist, create a minimal one
  if (!existing || existing.trim() === '') {
    var lines = [];
    lines.push("import { JsonAsset, resources } from 'cc';");
    lines.push("import { " + interfaceName + " } from './Configs/" + pascalCase(schemaType) + "Config';");
    lines.push('');
    lines.push('export class ConfigLoader {');
    lines.push('  private static _cache: Map<string, any> = new Map();');
    lines.push('');
    lines.push('  public static async load<T = any>(configPath: string): Promise<T> {');
    lines.push('    if (ConfigLoader._cache.has(configPath)) {');
    lines.push('      return ConfigLoader._cache.get(configPath) as T;');
    lines.push('    }');
    lines.push('    return new Promise<T>((resolve, reject) => {');
    lines.push('      resources.load(configPath, JsonAsset, (err, jsonAsset) => {');
    lines.push('        if (err) {');
    lines.push("          reject(new Error('ConfigLoader: failed to load \"' + configPath + '\": ' + err.message));");
    lines.push('          return;');
    lines.push('        }');
    lines.push('        const data = jsonAsset.json as T;');
    lines.push('        ConfigLoader._cache.set(configPath, data);');
    lines.push('        resolve(data);');
    lines.push('      });');
    lines.push('    });');
    lines.push('  }');
    lines.push('');
    lines.push('  public static get<T = any>(configPath: string): T | undefined {');
    lines.push('    return ConfigLoader._cache.get(configPath) as T | undefined;');
    lines.push('  }');
    lines.push('');
    lines.push('  public static clearCache(): void {');
    lines.push('    ConfigLoader._cache.clear();');
    lines.push('  }');
    lines.push(method);
    lines.push('}');
    lines.push('');
    return lines.join('\n');
  }

  // If loader exists, inject import and method if not already present
  var updated = existing;

  // Add import if not present
  var importStatement = "import { " + interfaceName + " } from './Configs/" + pascalCase(schemaType) + "Config';";
  if (updated.indexOf(interfaceName) === -1) {
    // Find the last import line and add after it
    var importLines = updated.match(/^import .+$/gm);
    if (importLines && importLines.length > 0) {
      var lastImport = importLines[importLines.length - 1];
      updated = updated.replace(lastImport, lastImport + '\n' + importStatement);
    } else {
      updated = importStatement + '\n' + updated;
    }
  }

  // Add method before closing brace of the class
  var methodName = camelCase('load_' + schemaType);
  if (updated.indexOf(methodName) === -1) {
    var closingBrace = updated.lastIndexOf('}');
    if (closingBrace >= 0) {
      updated = updated.slice(0, closingBrace) + method + '\n' + updated.slice(closingBrace);
    }
  }

  return updated;
}

// ---------------------------------------------------------------------------
// Validation
// ---------------------------------------------------------------------------

function validateSchemaData(schemaType, schemaData) {
  var errors = [];

  if (!schemaData) {
    errors.push('schemaData is required.');
    return errors;
  }

  if (typeof schemaData !== 'object') {
    errors.push('schemaData must be an object.');
    return errors;
  }

  // Validate field type declarations if fields/properties are present
  var fields = schemaData.fields || schemaData.properties;
  if (fields && typeof fields === 'object') {
    var validTypes = ['float', 'int', 'number', 'string', 'bool', 'boolean', 'enum', 'array', 'object'];
    var keys = Object.keys(fields);
    for (var i = 0; i < keys.length; i++) {
      var field = fields[keys[i]];
      if (field && field.type && validTypes.indexOf(field.type) === -1) {
        errors.push("Field '" + keys[i] + "' has unknown type '" + field.type + "'. Valid types: " + validTypes.join(', ') + '.');
      }
      if (field && field.type === 'enum' && (!field.enumValues || !Array.isArray(field.enumValues) || field.enumValues.length === 0)) {
        errors.push("Field '" + keys[i] + "' is an enum but has no enumValues array.");
      }
    }
  }

  return errors;
}

