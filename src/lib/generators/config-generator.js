// Public API
// ---------------------------------------------------------------------------

/**
 * Generate JSON config files and TypeScript interfaces from GDD Schema data.
 *
 * @param {object} args
 * @param {string} args.projectPath - Absolute path to the Cocos project root.
 * @param {string} args.schemaType - One of the supported schema types or 'custom'.
 * @param {object} args.schemaData - The schema definition or raw data object.
 * @param {string} [args.outputDir] - Override output directory for JSON configs (default: assets/Configs).
 * @param {boolean} [args.generateInterface=true] - Whether to generate a TypeScript interface file.
 * @param {boolean} [args.generateLoader=true] - Whether to update ConfigLoader.ts with a new load method.
 * @returns {Promise<{success: boolean, files: string[], validationErrors: string[], error?: string}>}
 */
async function generateConfigFiles(args) {
  var projectPath = args ? args.projectPath : undefined;
  var schemaType = args ? args.schemaType : undefined;
  var schemaData = args ? args.schemaData : undefined;
  var outputDir = args ? args.outputDir : undefined;
  var generateInterface = args && args.generateInterface !== undefined ? args.generateInterface : true;
  var generateLoader = args && args.generateLoader !== undefined ? args.generateLoader : true;

  // --- Validation -----------------------------------------------------------
  if (!projectPath || typeof projectPath !== 'string') {
    return { success: false, files: [], validationErrors: ['projectPath is required and must be a string.'] };
  }
  if (!schemaType || typeof schemaType !== 'string') {
    return { success: false, files: [], validationErrors: ['schemaType is required.'] };
  }
  if (schemaType !== 'custom' && SCHEMA_TYPES.indexOf(schemaType) === -1) {
    return { success: false, files: [], validationErrors: ['schemaType must be one of: ' + SCHEMA_TYPES.join(', ') + ', or "custom".'] };
  }

  var validationErrors = validateSchemaData(schemaType, schemaData);
  if (validationErrors.length > 0 && !schemaData.data) {
    // Only fail on validation errors if we're generating from schema, not from raw data
    return { success: false, files: [], validationErrors: validationErrors };
  }

  try {
    var files = [];

    // Determine output paths
    var configDir = outputDir || path.join(projectPath, 'assets/Configs');
    var jsonPath = path.join(configDir, schemaType + '.json');
    var scriptsDir = path.join(projectPath, 'assets/Scripts/Data');
    var configsTsDir = path.join(scriptsDir, 'Configs');
    var interfacePath = path.join(configsTsDir, pascalCase(schemaType) + 'Config.ts');
    var loaderPath = path.join(scriptsDir, 'ConfigLoader.ts');

    // 1. Generate JSON config
    var configData = generateDefaultConfigData(schemaType, schemaData);
    writeFileSafe(jsonPath, JSON.stringify(configData, null, 2));
    files.push(path.relative(projectPath, jsonPath));

    // 2. Generate TypeScript interface
    if (generateInterface) {
      var interfaceContent = generateInterfaceFile(schemaType, schemaData);
      writeFileSafe(interfacePath, interfaceContent);
      files.push(path.relative(projectPath, interfacePath));
    }

    // 3. Update ConfigLoader
    if (generateLoader) {
      var loaderContent = updateConfigLoader(loaderPath, schemaType);
      writeFileSafe(loaderPath, loaderContent);
      files.push(path.relative(projectPath, loaderPath));
    }

    return {
      success: true,
      files: files,
      validationErrors: validationErrors,
    };
  } catch (err) {
    return {
      success: false,
      files: [],
      validationErrors: [err.message],
    };
  }
}

module.exports = {
  generateConfigFiles,
  SCHEMA_TYPES,
};
