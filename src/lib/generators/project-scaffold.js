/**
 * Generate a Cocos Creator 3.8 project directory structure based on game type
 * and architecture pattern.
 *
 * @param {object} args
 * @param {string} args.projectPath - Absolute path where the project will be scaffolded.
 * @param {string} args.gameType - One of RPG, action, strategy, puzzle, simulation, platformer, card, roguelike.
 * @param {string} [args.architecture='component'] - One of MVC, ECS, component.
 * @param {string[]} [args.features=[]] - Array of feature flags.
 * @returns {Promise<{success: boolean, createdFiles: string[], nextSteps: string[], error?: string}>}
 */
async function generateProjectScaffold(args) {
  const {
    projectPath,
    gameType,
    architecture = 'component',
    features = [],
  } = args || {};

  // --- Validation -----------------------------------------------------------
  if (!projectPath || typeof projectPath !== 'string') {
    return { success: false, createdFiles: [], nextSteps: [], error: 'projectPath is required and must be a string.' };
  }
  if (!gameType || !GAME_TYPES.includes(gameType)) {
    return { success: false, createdFiles: [], nextSteps: [], error: `gameType is required and must be one of: ${GAME_TYPES.join(', ')}.` };
  }
  if (!ARCHITECTURES.includes(architecture)) {
    return { success: false, createdFiles: [], nextSteps: [], error: `architecture must be one of: ${ARCHITECTURES.join(', ')}.` };
  }
  const invalidFeatures = features.filter((f) => !FEATURES.includes(f));
  if (invalidFeatures.length > 0) {
    return { success: false, createdFiles: [], nextSteps: [], error: `Unknown features: ${invalidFeatures.join(', ')}. Valid: ${FEATURES.join(', ')}.` };
  }

  try {
    // --- Create directories -------------------------------------------------
    const dirs = buildDirectoryMap(features);
    for (const dir of dirs) {
      ensureDir(path.join(projectPath, dir));
    }

    // --- Write files --------------------------------------------------------
    const fileMap = buildFileMap(projectPath, gameType, architecture, features);
    const createdFiles = [];
    for (const [filePath, content] of Object.entries(fileMap)) {
      writeFileSafe(filePath, content);
      createdFiles.push(path.relative(projectPath, filePath));
    }

    // --- Build next steps ---------------------------------------------------
    const nextSteps = [
      'Open the project in Cocos Creator 3.8 to import assets.',
      'Review generated scripts in assets/Scripts/ and customize for your game.',
      'Set up GameManager in the Main scene as the root node component.',
    ];
    if (features.includes('save')) {
      nextSteps.push('Configure SaveSystem slot count and versioning in Data/SaveSystem.ts.');
    }
    if (features.includes('network')) {
      nextSteps.push('Update NetworkManager.connect() with your server URL.');
    }
    if (features.includes('combat')) {
      nextSteps.push('Extend CharacterBase with game-specific stats and abilities.');
    }
    if (features.includes('localization')) {
      nextSteps.push('Load language JSON files and call Localization.loadStrings() at startup.');
    }
    nextSteps.push('Add art assets to assets/Art/ and audio clips to assets/Audio/.');

    return { success: true, createdFiles, nextSteps };
  } catch (err) {
    return { success: false, createdFiles: [], nextSteps: [], error: err.message };
  }
}

