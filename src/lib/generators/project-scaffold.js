function templateEventBus() {
  return `${tsHeader('Global event bus for decoupled communication between modules.')}
type EventCallback = (...args: any[]) => void;

interface EventEntry {
  callback: EventCallback;
  target: any;
  once: boolean;
}

export class EventBus {
  private static _listeners: Map<string, EventEntry[]> = new Map();

  /** Register a persistent listener for an event. */
  public static on(event: string, callback: EventCallback, target?: any): void {
    const list = EventBus._listeners.get(event) || [];
    list.push({ callback, target: target || null, once: false });
    EventBus._listeners.set(event, list);
  }

  /** Register a one-shot listener that auto-removes after first invocation. */
  public static once(event: string, callback: EventCallback, target?: any): void {
    const list = EventBus._listeners.get(event) || [];
    list.push({ callback, target: target || null, once: true });
    EventBus._listeners.set(event, list);
  }

  /** Remove a specific listener, or all listeners for a target on this event. */
  public static off(event: string, callback?: EventCallback, target?: any): void {
    const list = EventBus._listeners.get(event);
    if (!list) return;
    if (!callback && !target) {
      EventBus._listeners.delete(event);
      return;
    }
    const filtered = list.filter((entry) => {
      if (callback && entry.callback === callback) return false;
      if (target && entry.target === target) return false;
      return true;
    });
    if (filtered.length === 0) {
      EventBus._listeners.delete(event);
    } else {
      EventBus._listeners.set(event, filtered);
    }
  }

  /** Emit an event with arbitrary arguments. */
  public static emit(event: string, ...args: any[]): void {
    const list = EventBus._listeners.get(event);
    if (!list) return;
    const toRemove: EventEntry[] = [];
    for (const entry of list) {
      entry.callback.apply(entry.target, args);
      if (entry.once) toRemove.push(entry);
    }
    if (toRemove.length > 0) {
      const remaining = list.filter((e) => !toRemove.includes(e));
      if (remaining.length === 0) {
        EventBus._listeners.delete(event);
      } else {
        EventBus._listeners.set(event, remaining);
      }
    }
  }

  /** Remove all listeners, optionally scoped to a single target. */
  public static clear(target?: any): void {
    if (!target) {
      EventBus._listeners.clear();
      return;
    }
    for (const [event, list] of EventBus._listeners.entries()) {
      const filtered = list.filter((e) => e.target !== target);
      if (filtered.length === 0) {
        EventBus._listeners.delete(event);
      } else {
        EventBus._listeners.set(event, filtered);
      }
    }
  }
}
`;
}

function templateObjectPool() {
  return `${tsHeader('Generic object pool to reduce garbage collection pressure.')}
export interface Poolable {
  recycle(): void;
}

export class ObjectPool<T extends Poolable> {
  private _pool: T[] = [];
  private _factory: () => T;
  private _maxSize: number;

  constructor(factory: () => T, initialSize: number = 0, maxSize: number = 256) {
    this._factory = factory;
    this._maxSize = maxSize;
    for (let i = 0; i < initialSize; i++) {
      this._pool.push(this._factory());
    }
  }

  /** Retrieve an object from the pool, or create a new one if empty. */
  public acquire(): T {
    if (this._pool.length > 0) {
      return this._pool.pop()!;
    }
    return this._factory();
  }

  /** Return an object to the pool. Calls recycle() before storing. */
  public release(obj: T): void {
    obj.recycle();
    if (this._pool.length < this._maxSize) {
      this._pool.push(obj);
    }
  }

  /** Current number of idle objects in the pool. */
  public get size(): number {
    return this._pool.length;
  }

  /** Drain the pool, calling recycle on each remaining object. */
  public clear(): void {
    for (const obj of this._pool) {
      obj.recycle();
    }
    this._pool.length = 0;
  }
}
`;
}

function templateStateMachineBase() {
  return `${tsHeader('Base state machine providing state registration, transitions, and lifecycle hooks.')}
export interface StateCallbacks {
  onEnter?: (prev: string) => void;
  onExit?: (next: string) => void;
  onUpdate?: (dt: number) => void;
}

export interface TransitionDef {
  from: string;
  to: string;
  trigger: string;
  condition?: () => boolean;
  action?: () => void;
  priority?: number;
}

export class StateMachineBase {
  protected _states: Map<string, StateCallbacks> = new Map();
  protected _transitions: TransitionDef[] = [];
  protected _currentState: string = '';
  protected _previousState: string = '';

  public get currentState(): string { return this._currentState; }
  public get previousState(): string { return this._previousState; }

  /** Register a named state with optional lifecycle callbacks. */
  public registerState(name: string, callbacks: StateCallbacks = {}): void {
    this._states.set(name, callbacks);
  }

  /** Add a transition rule between two states. */
  public addTransition(def: TransitionDef): void {
    this._transitions.push(def);
    this._transitions.sort((a, b) => (b.priority || 0) - (a.priority || 0));
  }

  /** Enter the initial state. Call once after registering states. */
  public start(initialState: string): void {
    if (!this._states.has(initialState)) {
      throw new Error(\`State "\${initialState}" is not registered.\`);
    }
    this._currentState = initialState;
    const cb = this._states.get(initialState);
    if (cb && cb.onEnter) cb.onEnter('');
  }

  /** Attempt a transition by trigger name. Returns true if a transition occurred. */
  public transition(trigger: string): boolean {
    for (const t of this._transitions) {
      if (t.from !== this._currentState || t.trigger !== trigger) continue;
      if (t.condition && !t.condition()) continue;
      if (!this._states.has(t.to)) continue;

      const fromCb = this._states.get(this._currentState);
      if (fromCb && fromCb.onExit) fromCb.onExit(t.to);

      if (t.action) t.action();

      this._previousState = this._currentState;
      this._currentState = t.to;

      const toCb = this._states.get(this._currentState);
      if (toCb && toCb.onEnter) toCb.onEnter(this._previousState);
      return true;
    }
    return false;
  }

  /** Forward update tick to the current state. */
  public update(dt: number): void {
    const cb = this._states.get(this._currentState);
    if (cb && cb.onUpdate) cb.onUpdate(dt);
  }
}
`;
}

