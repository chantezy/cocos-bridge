function templateConfigLoader() {
  return `${tsHeader('Config loader for reading JSON config files from the Configs directory.')}
import { JsonAsset, resources } from 'cc';

export class ConfigLoader {
  private static _cache: Map<string, any> = new Map();

  /** Load a JSON config by relative path (without extension) under assets/Configs. */
  public static async load<T = any>(configPath: string): Promise<T> {
    if (ConfigLoader._cache.has(configPath)) {
      return ConfigLoader._cache.get(configPath) as T;
    }
    return new Promise<T>((resolve, reject) => {
      resources.load(configPath, JsonAsset, (err, jsonAsset) => {
        if (err) {
          reject(new Error(\`ConfigLoader: failed to load "\${configPath}": \${err.message}\`));
          return;
        }
        const data = jsonAsset.json as T;
        ConfigLoader._cache.set(configPath, data);
        resolve(data);
      });
    });
  }

  /** Retrieve a previously loaded config from cache. Returns undefined if not loaded. */
  public static get<T = any>(configPath: string): T | undefined {
    return ConfigLoader._cache.get(configPath) as T | undefined;
  }

  /** Clear the entire config cache. */
  public static clearCache(): void {
    ConfigLoader._cache.clear();
  }
}
`;
}

function templateSaveSystem() {
  return `${tsHeader('Save system using sys.localStorage for persistent data storage.')}
import { sys } from 'cc';
import { EventBus } from '../Core/EventBus';

export interface SaveData {
  version: number;
  timestamp: number;
  data: Record<string, any>;
}

export class SaveSystem {
  private static readonly SAVE_KEY_PREFIX = 'game_save_';
  private static readonly SAVE_VERSION = 1;
  private static readonly MAX_SLOTS = 10;

  /** Save data to a numbered slot. */
  public static save(slot: number, data: Record<string, any>): boolean {
    if (slot < 0 || slot >= SaveSystem.MAX_SLOTS) {
      console.warn(\`[SaveSystem] Slot \${slot} out of range (0-\${SaveSystem.MAX_SLOTS - 1}).\`);
      return false;
    }
    const payload: SaveData = {
      version: SaveSystem.SAVE_VERSION,
      timestamp: Date.now(),
      data,
    };
    try {
      sys.localStorage.setItem(SaveSystem.SAVE_KEY_PREFIX + slot, JSON.stringify(payload));
      EventBus.emit('GAME_SAVED', slot);
      return true;
    } catch (e) {
      console.error('[SaveSystem] Save failed:', e);
      return false;
    }
  }

  /** Load data from a numbered slot. Returns null if empty or corrupt. */
  public static load(slot: number): SaveData | null {
    try {
      const raw = sys.localStorage.getItem(SaveSystem.SAVE_KEY_PREFIX + slot);
      if (!raw) return null;
      return JSON.parse(raw) as SaveData;
    } catch (e) {
      console.error('[SaveSystem] Load failed:', e);
      return null;
    }
  }

  /** Delete a save slot. */
  public static delete(slot: number): void {
    sys.localStorage.removeItem(SaveSystem.SAVE_KEY_PREFIX + slot);
  }

  /** Check whether a slot has data. */
  public static hasSave(slot: number): boolean {
    return sys.localStorage.getItem(SaveSystem.SAVE_KEY_PREFIX + slot) !== null;
  }
}
`;
}

